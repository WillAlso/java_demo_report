public class Calculator {
    double add(double num1,double num2){
        return num1+num2;
    }
    double minus(double num1,double num2){
        return num1-num2;
    }
    double multiple(double num1,double num2){
        return num1*num2;
    }
    double divide(double num1,double num2){
        return num1/num2;
    }
}
